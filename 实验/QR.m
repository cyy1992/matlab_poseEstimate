%   此处显示详细说明
R = [1-2*(q(3)*q(3)+q(4)*q(4)) 2*(q(2)*q(3)-q(4)*q(1)) 2*(q(2)*q(4)+q(3)*q(1));
     2*(q(2)*q(3)+q(4)*q(1)) 1-2*(q(2)*q(2)+q(4)*q(4)) 2*(q(3)*q(4)-q(2)*q(1));
     2*(q(2)*q(4)-q(3)*q(1)) 2*(q(3)*q(4) + q(2)*q(1)) 1 - 2*(q(2)*q(2)+q(3)*q(3))];
end

